function New-Issue {
    param(
        [string]$Title,
        [string]$Body,
        [string]$Label,
        [string]$Milestone
    )
    $tmp = New-TemporaryFile
    try {
        [System.IO.File]::WriteAllText($tmp.FullName, $Body, (New-Object System.Text.UTF8Encoding($false)))
        $ghArgs = @('issue','create','--title',$Title,'--body-file',$tmp.FullName,'--label',$Label)
        if ($Milestone) { $ghArgs += @('--milestone',$Milestone) }
        Write-Host "Creating: $Title" -ForegroundColor Cyan
        & gh @ghArgs
        if ($LASTEXITCODE -ne 0) { throw "gh failed for: $Title" }
    } finally {
        Remove-Item $tmp.FullName -Force -ErrorAction SilentlyContinue
    }
}

